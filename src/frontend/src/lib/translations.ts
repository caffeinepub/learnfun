// Proxy to provide English fallback for missing keys
export function useTranslation(language: LanguageCode) {
  const languageTranslations = translations[language] || translations.en;
  
  return new Proxy(languageTranslations, {
    get(target, prop: string) {
      if (prop in target) {
        return target[prop as keyof typeof target];
      }
      // Fallback to English if key doesn't exist
      return translations.en[prop as keyof typeof translations.en] || prop;
    }
  });
}

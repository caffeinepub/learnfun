export function useTranslation(lang: LanguageCode) {
  const baseTranslations = translations[lang];
  const fallback = translations.en;
  
  // Merge with English fallback for missing keys
  return new Proxy(baseTranslations, {
    get(target, prop: string) {
      return target[prop as keyof typeof target] || fallback[prop as keyof typeof fallback] || prop;
    }
  }) as typeof commonTranslations;
}
